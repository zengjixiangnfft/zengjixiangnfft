#include "main.h"
/*-----------------------------------------------------------------------
|                                 DATA                                  |
-----------------------------------------------------------------------*/
extern bit Time500MsFlag;
/*-----------------------------------------------------------------------
|                               FUNCTION                                |
-----------------------------------------------------------------------*/

/**
  * @name    LOGO
  * @brief   printf logo
  * @param   None
  * @return  None
***/
void LOGO(void)
{
//	printf("Timer0 500MS \r\n");
  UART1_Isr_Send_String("Uart1\r\n");
  UART1_Isr_Send_String("Uart2\r\n");
}

/**
  * @name    main
  * @brief   main program
  * @param   None
  * @return  None
***/
int main(void)
{
	STC8x_System_Init();

	for(;;)
	{
      if(Time500MsFlag == 1)
      {
        Time500MsFlag = 0;
        LOGO();
      }
	}

}



