/**
  * @name    Task1_Led
  * @brief   main program
  * @param   None
  * @return  None
***/
void Task1_Led(void)
{
	GPIO_TOGGLE_PIN(GPIO_P5,Pin_5);
}
/**
  * @name    main
  * @brief   main program
  * @param   None
  * @return  None
***/
int main(void)
{
    STC8x_System_Init();
	
	TMT_Init();
	
	if(TMT.Create(Task1_Led,500) == Create_Success)
	{
		DEBUG_LOG("success \r\n");
	}
	else
	{
		DEBUG_LOG("fail  \r\n");		
	}
	
    for(;;)
    {
		TMT.Run();
    }

}




